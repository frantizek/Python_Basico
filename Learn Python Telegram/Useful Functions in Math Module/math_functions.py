import math

def get_absolute_value(x):
    return math.fabs(x)

def get_remainder(x, y):
    return math.fmod(x, y)

def get_power(x, y):
    return math.pow(x, y)

def get_ceiling(x):
    return math.ceil(x)

def get_floor(x):
    return math.floor(x)

def get_fractional_and_integer_parts(x):
    return math.modf(x)

def get_factorial(x):
    return math.factorial(x)