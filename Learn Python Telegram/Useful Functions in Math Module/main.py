import math

# Function: fabs(x)
# Description: Returns the absolute value of x (as a float)
# Example: 
# print(math.fabs(-5)) # Output 5.0
print(f"math.fabs(-5) -> Expected 5.0, Got: {math.fabs(-5)}")


# Function: fmod(x,y)
# Description: Returns the remainder when x is divided by y.
# Example: 
# print(math.fmod(5,2)) # Output 1.0
print(f"math.fmod(5,2) -> Expected 1.0, Got: {math.fmod(5,2)}")

# Function: pow(x,y) 
# Description: Returns x raised to the power of y.
# Example: 
# print(math.pow(5, 3)) # Output 125.0
print(f"math.pow(5, 3) -> Expected 125.0, Got: {math.pow(5, 3)}")

# Function: ceil(x)
# Description: Returns a number UP to the nearest integer.
# Example: 
# print(math.ceil(5.2)) # Output 6
print(f"math.ceil(5.2) -> Expected 6, Got: {math.ceil(5.2)}")

# Function: floor(x)
# Description: Rounds a number DOWN to the nearest integer.
# Example: 
# print(math.floor(5.2)) # Output 5
print(f"math.floor(5.2) -> Expected 5, Got: {math.floor(5.2)}")

# Function: modf(x)
# Description: Returns the fractional and integer parts of x in a two item touple.
# Example: 
# print(math.modf(50.5)) # Output (0.5, 50.0)
print(f"math.modf(50.5) -> Expected (0.5, 50.0), Got: {math.modf(50.5)}")

# Function: factorial(x)
# Description: Returns the factorial of x.
# Example: 
# print(math.factorial(5)) # Output 120
print(f"math.factorial(5) -> Expected 120, Got: {math.factorial(5)}")
