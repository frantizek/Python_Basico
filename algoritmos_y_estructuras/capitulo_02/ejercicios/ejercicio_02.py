# Implementar una función para calcular el producto de dos números enteros dados.

def producto_de_dos_numeros(primer_numero: int, segundo_numero: int) -> float:
    """Producto de dos numeros enteros."""
    return primer_numero * segundo_numero


def main():
    print(producto_de_dos_numeros(9,9))


if __name__ == '__main__':
    main()
