# Calculate the compound interest for a given principal amount, interest rate and time period.

# The formula you can use to calculate compound interest for a given principal amount, interest rate, and time period is:
#
# A = P (1 + r/n) ^ nt
#
# where:
#
# A is the future value (total amount) after the time period, including principal and interest.
# P is the initial principal amount.
# r is the annual interest rate (as a decimal). For example, if the interest rate is 5%, then r = 0.05.
# n is the number of times the interest is compounded in a year. For example, if interest is compounded monthly, then n = 12.
# t is the number of years.
